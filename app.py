def classify_intent(user_message: str) -> str:
    prompt = (
        "Voc√™ √© um assistente que classifica a inten√ß√£o do usu√°rio. "
        "Responda APENAS com: plano, agendamento, sair ou desconhecido.\n"
        f"Pergunta do usu√°rio: {user_message}\nInten√ß√£o:"
    )
    try:
        response = llm.invoke([HumanMessage(content=prompt)])
        intent = response.content.strip().lower()
        for option in ["plano", "agendamento", "sair", "desconhecido"]:
            if option in intent:
                logger.info(f"Inten√ß√£o: {option}")
                return option
    except Exception as e:
        logger.error("Erro na classifica√ß√£o: %s", e)
    return "desconhecido"

